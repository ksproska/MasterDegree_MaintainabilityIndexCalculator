public class Example {
    public void example() {
        int a = 1, b = 2, c = 3, d = 4, e = 5;
        double f = 1.0, g = 2.0, h = 3.0, i = 4.0, j = 5.0;
        String k = "alpha", l = "beta", m = "gamma", n = "delta", o = "epsilon";
        int sum = a + b + c + d + e;
        double product = f * g * h * i * j;
        String result = k + l + m + n + o;
        System.out.println("Sum: " + sum);
        System.out.println("Product: " + product);
        System.out.println("Concatenation: " + result);
    }
}
